// concat is used to merge two or more arrays.
// does not update the original arrays, returns a new array

let fruits = ['apple', 'banana']
let veggies = ['carrots', 'broccoli']
let meats = ['steak', 'turkey']

console.log(fruits.concat(veggies))

allFoods = fruits.concat(veggies, meats)
console.log(allFoods)