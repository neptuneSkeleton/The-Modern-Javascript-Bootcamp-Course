// Includes - checks if array includes a certain value - returns true or false
// IndexOf - checks if a value is in the array and returns the index of the value

let foods = ['apple', 'banana', 'carrots', 'broccoli', 'steak', 'turkey']

foods.includes('apples') // true
foods.includes('fish') // false

foods.indexOf('carrots') // 2
foods.indexOf('fish') // -1