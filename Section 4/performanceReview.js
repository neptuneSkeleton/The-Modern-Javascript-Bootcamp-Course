// 3 - Superstar
// 2 - meets expectations
// 1 - needs improvement
// anything else - ?

let rating = 2

if (rating === 3) {
    console.log("YOU ARE A SUPERSTAR!")
}
else if (rating === 2) {
    console.log("Meets expectations")
}
else if (rating === 1) {
    console.log("needs improvement")
}
else {
    console.log("INVALID RATING")
}